package model;

public class CarCar {
	private String userid;
	private String goodid;
	private String count;
	private String carmoney;
	private String goodname;
	private String goodprice;
	private String goodimg;
	private String carid;
	
	public CarCar(String userid, String goodid, String count, String carmoney,
			String goodname, String goodprice, String goodimg, String carid) {
		super();
		this.userid = userid;
		this.goodid = goodid;
		this.count = count;
		this.carmoney = carmoney;
		this.goodname = goodname;
		this.goodprice = goodprice;
		this.goodimg = goodimg;
		this.carid = carid;
	}
	public String getCarid() {
		return carid;
	}
	public void setCarid(String carid) {
		this.carid = carid;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getGoodid() {
		return goodid;
	}
	public void setGoodid(String goodid) {
		this.goodid = goodid;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getCarmoney() {
		return carmoney;
	}
	public void setCarmoney(String carmoney) {
		this.carmoney = carmoney;
	}
	public String getGoodname() {
		return goodname;
	}
	public void setGoodname(String goodname) {
		this.goodname = goodname;
	}
	public String getGoodprice() {
		return goodprice;
	}
	public void setGoodprice(String goodprice) {
		this.goodprice = goodprice;
	}
	public String getGoodimg() {
		return goodimg;
	}
	public void setGoodimg(String goodimg) {
		this.goodimg = goodimg;
	}
	public CarCar(String userid, String goodid, String count, String carmoney,
			String goodname, String goodprice, String goodimg) {
		this.userid = userid;
		this.goodid = goodid;
		this.count = count;
		this.carmoney = carmoney;
		this.goodname = goodname;
		this.goodprice = goodprice;
		this.goodimg = goodimg;
	}

}
